package com.gniot.business;

public class BasicCalculator {
	public double addtion(double a, double b) {

		System.out.println("In the addtion Method");
		return a + b;
	}

	public double substraction(double a, double b) {
		System.out.println("In the Substraction Method");
		return a - b;
	}

	public double multiply(double a, double b) {
		System.out.println("In the multiply method");
		return a * b;
	}

	public double devide(double a, double b) {
		System.out.println("In the Devide method");
		return a / b;
	}

}
